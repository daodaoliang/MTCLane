package com.hgits.exception;

/**
 * MTC异常处理类
 * 
 * @author wh
 *
 */
public class MTCException extends Exception {
	/**
	 * 
	 */
	private static final long serialVersionUID = -1332355750475815889L;

	public MTCException() {
	}

	public MTCException(String s) {
		super(s);
	}

	public MTCException(String s, Throwable throwable) {
		super(s, throwable);
	}

	public MTCException(Throwable throwable) {
		super(throwable);
	}
}
