package com.hgits.vo;

/**
 *轴
 * @author Wang Guodong
 */
public class Axle {
    private String axleType;//轴型 01 单轴单轮 02 单轴双轮
    private double weight;//轴重单位10千克

    public String getAxleType() {
        return axleType;
    }

    public void setAxleType(String axleType) {
        this.axleType = axleType;
    }

    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }

    @Override
    public String toString() {
        return "Axle{" + "axleType=" + axleType + ", weight=" + weight + '}';
    }

    public Axle(String axleType, double weight) {
        this.axleType = axleType;
        this.weight = weight;
    }

    public Axle() {
    }
    
    
}
