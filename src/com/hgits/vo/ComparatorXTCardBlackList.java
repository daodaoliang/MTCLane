package com.hgits.vo;
import java.util.Comparator;

/**
 * ETC卡黑名单排序
 * @author wh
 *
 */
public class ComparatorXTCardBlackList implements Comparator<XTCardBlackList> {

	public int compare(XTCardBlackList arg0, XTCardBlackList arg1) {
		// 只比较ETC卡卡号

		int flag = arg0.getXtCardNo().compareTo(arg1.getXtCardNo());
		return flag;
	}

}