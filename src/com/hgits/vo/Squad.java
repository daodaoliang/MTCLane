package com.hgits.vo;

import java.util.Date;

/**
 * 工班编码表
 * 
 * @author wh
 *
 */
public class Squad {

	private Integer squadId;

	private String squadName;

	private Date startTime;

	private Date endTime;

	private Date startDate;

	private Integer timeDiffInteger;

	public Integer getSquadId() {
		return squadId;
	}

	public void setSquadId(Integer squadId) {
		this.squadId = squadId;
	}

	public String getSquadName() {
		return squadName;
	}

	public void setSquadName(String squadName) {
		this.squadName = squadName;
	}

	public Date getStartTime() {
		return startTime;
	}

	public void setStartTime(Date startTime) {
		this.startTime = startTime;
	}

	public Date getEndTime() {
		return endTime;
	}

	public void setEndTime(Date endTime) {
		this.endTime = endTime;
	}

	public Date getStartDate() {
		return startDate;
	}

	public void setStartDate(Date startDate) {
		this.startDate = startDate;
	}

	public Integer getTimeDiffInteger() {
		return timeDiffInteger;
	}

	public void setTimeDiffInteger(Integer timeDiffInteger) {
		this.timeDiffInteger = timeDiffInteger;
	}
}
