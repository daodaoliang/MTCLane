package com.hgits.vo;

import java.util.Date;

/**
 * 国家或省级高速公路编号实体类
 *
 * @author wh
 *
 */
public class NationalRoad {

    private String nationalRoadNo;
    private String nationalRoadName;
    private Integer version;
    private Date startTime;
    private Integer status;

    public String getNationalRoadNo() {
        return nationalRoadNo;
    }

    public void setNationalRoadNo(String nationalRoadNo) {
        this.nationalRoadNo = nationalRoadNo;
    }

    public String getNationalRoadName() {
        return nationalRoadName;
    }

    public void setNationalRoadName(String nationalRoadName) {
        this.nationalRoadName = nationalRoadName;
    }

    public Integer getVersion() {
        return version;
    }

    public void setVersion(Integer version) {
        this.version = version;
    }

    public Date getStartTime() {
        return startTime;
    }

    public void setStartTime(Date startTime) {
        this.startTime = startTime;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }
}
