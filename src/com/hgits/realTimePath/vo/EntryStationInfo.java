package com.hgits.realTimePath.vo;

/**
 * 车辆入口站点信息
 *
 * @author Wang Guodong
 */
public class EntryStationInfo {

    private String StationId;//车辆入口收费站的编号，编号由路段网络号+站号组成（如羊楼司：0501，德山：4579）
    private String EntryTime;//车辆通过收费站进入高速公路的时间，（格式为:YYYYMMDD24HHMISS例：20140506215804）
    private String ImagePath;//入口车辆图片（大图）URL，如：http://192.168.1.1/pic/stest.jpg

    public String getStationId() {
        return StationId;
    }

    public void setStationId(String StationId) {
        this.StationId = StationId;
    }

    public String getEntryTime() {
        return EntryTime;
    }

    public void setEntryTime(String EntryTime) {
        this.EntryTime = EntryTime;
    }

    public String getImagePath() {
        return ImagePath;
    }

    public void setImagePath(String ImagePath) {
        this.ImagePath = ImagePath;
    }

    @Override
    public String toString() {
        return "EntryStationInfo{" + "StationId=" + StationId + ", EntryTime=" + EntryTime + ", ImagePath=" + ImagePath + '}';
    }

}
