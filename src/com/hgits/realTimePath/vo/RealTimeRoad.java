package com.hgits.realTimePath.vo;

/**
 *
 * @author Wang Guodong
 */
public class RealTimeRoad {
    private String rtRoadid;//路径识别规定的路段编号
    private String uniqueRoadid;//实际的路段唯一编号
    private double dis;//里程

    public String getRtRoadid() {
        return rtRoadid;
    }

    public void setRtRoadid(String rtRoadid) {
        this.rtRoadid = rtRoadid;
    }

    public String getUniqueRoadid() {
        return uniqueRoadid;
    }

    public void setUniqueRoadid(String uniqueRoadid) {
        this.uniqueRoadid = uniqueRoadid;
    }




    public double getDis() {
        return dis;
    }

    public void setDis(double dis) {
        this.dis = dis;
    }

    @Override
    public String toString() {
        return "RealTimeRoad{" + "rtRoadid=" + rtRoadid + ", uniqueRoadid=" + uniqueRoadid + ", dis=" + dis + '}';
    }

    
    
}
